# Lambda Function
    # É uma função (pequena) sem nome 
    # Pode ter vários argumentos mas somente 1 expressão 
    # Muito utilizada dentro de outras funções 
    # Código mais "Clean"

'''
def soma (x):
    return x + 10

print (soma(2))
'''

somar10 = lambda x: x+10

print(somar10(2))

