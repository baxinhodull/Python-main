# Pedi ao usuário que digite dois números 

num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))

# Realizar os calculos matemáticos 

soma = num1 + num2
sub = num1 - num2
multi = num1 * num2
div = num1 / num2
exponenciacao = num1 ** num2

# Imprimindo no console 

print (f'Soma: {soma}')
print (f'Subtração: {sub}')
print (f'Multiplicação: {multi}')
print (f'Divisão: {div}')
print (f'Exponenciação: {exponenciacao}')
